    <div class="field field--name-body field--type-text-with-summary field--label-hidden"><div class="field__items"><div class="field__item even" property="content:encoded"><h2>Masterstudiengang Web Science</h2>
<p>Das Institut WeST bietet das erste <a href="/de/mws">internationale, interdisziplinäre Web Science Masterprogramm</a> in Deutschland an. Auf den Unterseiten zu unserem Masterstudiengang finden Studieninteressierte und Studierende alle wichtigen Informationen - von allgemeinen Informationen zum Studiengang über&nbsp;die Bewerbungsprozedur&nbsp;bis zu einer Liste der anrechenbaren Modulen.&nbsp;</p>
<p><iframe allowfullscreen="" frameborder="0" height="315" src="//www.youtube.com/embed/9SqUngY0e5U" width="560"></iframe></p>
<h2>Für alle Studierenden aus dem Fachbereich Informatik</h2>
<p>WeST bietet - auch über den Masterstudiengang Web Science hinaus -&nbsp;verschiedene&nbsp;<a href="/studium/lehrveranstaltungen">Lehrveranstaltungen</a>&nbsp;für den Fachbereich Informatik an. Dort finden Studierende auch&nbsp;Prüfungstermine&nbsp;und Materialien zu Vorlesungen sowie ein Archiv der vergangenen Semester.&nbsp;</p>
<p>Eine besondere Rolle spielt dabei das <a href="ueber-uns/oberseminar">Oberseminar</a> des Instituts WeST.&nbsp;Hier stellen Mitglieder von WeST, Gesis oder externe Gäste ihre Forschungsergebnisse vor. Das Oberseminar ist öffentlich und Gäste sind jederzeit willkommen!</p>
<p>Wir bieten <a href="/de/studium/bachelor-theses">Abschlussarbeiten im Bachelor</a> sowie <a href="/de/studium/master-theses">im </a><a"><a href="/de/studium/master-theses">Master</a> an. Die ausgeschriebenen Arbeiten decken das gesamte Spektrum der Web Science ab - vom&nbsp;Sematic&nbsp;Web</a">&nbsp;über Suchmaschinen (information retrieval), Social&nbsp;Web&nbsp;und Social Media, Data Mining im Web und Human Computing.</p>
</div></div></div>  </div>

